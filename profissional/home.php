		<p>&emsp;
			O sistema foi desenvolvido para o Centro Acadêmico com o objetivo de fornecer uma ferramenta para possibilitar um feedback e uma autoavaliação para os professores da Universidade.
			Os resultados eram calculados e exibidos online com o objetivo de pressionar o professor a melhorar seus métodos.
		</p>
		<p>&emsp;
			
		</p>
	</h4>
</div>

<div><h3>Funcionamento</h3></div>
<div>
	<h4>
		<p>&emsp;
			O sistema de avaliação de profesores tinha 2 finalidades: 
			Avaliar  os professores e consultar os resultados de avaliações antigas.
			O resultado da avaliação era exibido publicamente ao fim do período para evitar retalhações de professores.
		</p>
		<p>&emsp;
			Um banco de dados foi feito para guardar as informações de professores e avaliações feitas.
			O programa computava todas as avaliações feitas no período e guardava os resultados de volta no banco de dados para que professores e alunos pudessem conferir.
			A validação era feita através de Matrícula da Universidade dificultava fraudes por parte de alunos(e professores!)
		</p>
		
	</h4>
</div>

<div><h3>Repercursão</h3></div>
<div>
	<h4>
		<p>&emsp;
			O desenvolvimento contou com a participação de 2 desenvolvedores e foi amplamente usado e divulgado pelos alunos e professores do Instituto de Matemática e Estatística.
			O envolvimento dos alunos com a avaliação foi muito boa.
			Com os professores não foi diferente, a grande maioria dos professores elogiaram o sistema e o usavam como ferramenta de consulta a respeito da percepção do aluno à qualidade de ensino.
			Casos como: Ameaça de processo por uso indevido de imagem, Pedidos de ocultar comentários do professor e Solicitações de alunos para incorporação do sistema ao site da UERJ;
			mostraram que o sistema balançou o Instituto e cumpriu muito bem o propósito desejado pelo Centro Acadêmico.
		</p>
		<p>&emsp;
			Após a saída da gestão, o sistema foi descontinuado e desativado.
			Mas as sugestões feitas pelo Diretório Central de Estudantes foram atendidas.
			Agora o Aluno Online, sistema da UERJ, contém avaliação geral de professores.
		</p>
	</h4>
</div>




