			Um diploma em um mercado competitivo deveria ser um bom estímulo para que universitários concluam seu curso.
			Mas os índices de abandono continuam altos.
		<p>&emsp;
			Certamente, existem vários métodos de motivacionais para isso.
			Porém, há algo interessante acontecendo na UERJ de iniciativa dos próprios alunos. 
