		<p>&emsp;
			É comum por lá, alunos da Engenharia ou Ciência da Computação criarem aplicações de auxílio para outros estudantes.
			O COMPUTAÇÃO-UERJ é um exemplo disso. Mas o TOP CRs foi como um pé na porta. 
		</p>
		<p>&emsp;
			Funciona assim, uma listagem de alunos é exibido sendo ordenado pelo Coeficiente de Rendimento, que é média de notas tiradas em nas disciplinas.
			Os nomes são ocultados da listagem para que não sejam identificados.
			Se o aluno quiser ver o Ranking de CRs, deverá deixar que os usuários vejam o seu nome também. 
		</p>
		<p>&emsp;
			Basicamente, o TOP-CRs estimula a competição.
		</p>
	</h4>
